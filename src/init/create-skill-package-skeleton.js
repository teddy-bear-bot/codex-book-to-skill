const fs = require('node:fs/promises');
const path = require('node:path');

function toDisplayName(skillId) {
  return skillId
    .split(/[-_]+/)
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ');
}

async function ensureTargetIsEmpty(targetDir) {
  try {
    const entries = await fs.readdir(targetDir);
    if (entries.length > 0) {
      throw new Error(`Target directory is not empty: ${targetDir}`);
    }
  } catch (error) {
    if (error.code === 'ENOENT') {
      return;
    }
    throw error;
  }
}

async function writeJson(filePath, value) {
  await fs.writeFile(filePath, `${JSON.stringify(value, null, 2)}\n`, 'utf8');
}

async function createSkillPackageSkeleton(targetDir, options = {}) {
  const resolvedTargetDir = path.resolve(targetDir);
  const skillId = options.id || path.basename(resolvedTargetDir);
  const displayName = options.name || toDisplayName(skillId);
  const version = options.version || '0.1.0';

  await ensureTargetIsEmpty(resolvedTargetDir);
  await fs.mkdir(path.join(resolvedTargetDir, 'adapters'), { recursive: true });
  await fs.mkdir(path.join(resolvedTargetDir, 'examples'), { recursive: true });

  await writeJson(path.join(resolvedTargetDir, 'skill.json'), {
    package_schema_version: '1.0.0',
    id: skillId,
    name: displayName,
    version,
    description: `${displayName} skill scaffold generated by book-to-skill init.`,
    license: 'Apache-2.0',
    skill_spec_file: 'spec.json',
    readme_file: 'README.md',
    entrypoints: {
      system_prompt_file: 'system.md',
      inputs_schema_file: 'inputs.schema.json',
    },
    installation: {
      mode: 'copy',
      default_install_root: '~/.book-to-skill/skills',
      install_subdir_pattern: '{id}@{version}',
    },
    adapters: [
      {
        platform: 'generic',
        manifest_file: 'adapters/generic.json',
        default: true,
      },
    ],
    compatibility: {
      book_to_skill_cli: '>=0.1.0',
      runtimes: ['generic-prompt-runtime'],
    },
  });

  await fs.writeFile(
    path.join(resolvedTargetDir, 'README.md'),
    `# ${displayName}\n\nGenerated by \`book-to-skill init\`.\n`,
    'utf8'
  );
  await fs.writeFile(
    path.join(resolvedTargetDir, 'system.md'),
    `You are the ${skillId} skill.\n\nFollow the workflow and constraints defined in this package.\n`,
    'utf8'
  );
  await writeJson(path.join(resolvedTargetDir, 'inputs.schema.json'), {
    type: 'object',
    additionalProperties: false,
    properties: {
      task: {
        type: 'string',
        description: 'The user task for this skill.',
      },
    },
    required: ['task'],
  });
  await writeJson(path.join(resolvedTargetDir, 'spec.json'), {
    skill_id: skillId,
    title: displayName,
    summary: `${displayName} distilled into a reusable skill package scaffold.`,
    core_principles: [
      'Keep outputs concise and actionable.',
      'Explain assumptions when context is missing.',
    ],
    workflow: [
      'Understand the user goal.',
      'Apply the skill-specific method.',
      'Return a structured result.',
    ],
    constraints: [
      'Do not fabricate unavailable source facts.',
    ],
    output_format: {
      type: 'markdown',
      sections: ['Answer', 'Notes'],
    },
  });
  await writeJson(path.join(resolvedTargetDir, 'adapters', 'generic.json'), {
    platform: 'generic',
    entry: 'system.md',
    notes: 'Default generic prompt adapter.',
  });
  await writeJson(path.join(resolvedTargetDir, 'examples', 'example-input.json'), {
    task: 'Describe what this skill should help with.',
  });

  return {
    targetDir: resolvedTargetDir,
    manifest: {
      id: skillId,
      name: displayName,
      version,
    },
  };
}

module.exports = {
  createSkillPackageSkeleton,
};
