const fs = require('node:fs/promises');
const os = require('node:os');
const path = require('node:path');

async function pathExists(targetPath) {
  try {
    await fs.access(targetPath);
    return true;
  } catch {
    return false;
  }
}

function expandHome(inputPath) {
  if (!inputPath) return inputPath;
  if (inputPath === '~') return os.homedir();
  if (inputPath.startsWith('~/')) {
    return path.join(os.homedir(), inputPath.slice(2));
  }
  return inputPath;
}

function isPathInsideRoot(rootPath, targetPath) {
  const relative = path.relative(rootPath, targetPath);
  return relative === '' || (!relative.startsWith('..') && !path.isAbsolute(relative));
}

function validateManifestId(rawId) {
  if (typeof rawId !== 'string') {
    throw new Error('invalid manifest id for codex target path: expected string');
  }

  const id = rawId.trim();
  if (!id) {
    throw new Error('invalid manifest id for codex target path: empty');
  }
  if (/[\\/]/.test(id) || id.includes('..') || /[\r\n]/.test(id)) {
    throw new Error(`invalid manifest id for codex target path: ${rawId}`);
  }

  return id;
}

function toSingleLineText(value) {
  return String(value ?? '').replace(/[\r\n]+/g, ' ').trim();
}

async function readInstalledManifest(installedSkill) {
  const manifestPath = path.join(installedSkill.installedPath, 'skill.json');
  return JSON.parse(await fs.readFile(manifestPath, 'utf8'));
}

function renderSkillMarkdown(manifest) {
  const name = validateManifestId(manifest.id);
  const descriptionSkillName = toSingleLineText(manifest.name);

  return [
    '---',
    `name: ${name}`,
    `description: Use when the user wants help applying the packaged ${descriptionSkillName} from book-to-skill.`,
    '---',
    '',
    'Use this skill when the user asks for this packaged capability.',
    'Read `references/system.md` for the full method and behavioral instructions.',
    'Read `references/inputs.schema.json` when you need expected input fields.',
    'Avoid reproducing copyrighted source text from packaged references.',
    'Follow the original constraints defined by the packaged skill.',
    '',
  ].join('\n');
}

async function ensureConfiguredFile(installedPath, relativeFile, entrypointField) {
  const installedRoot = path.resolve(installedPath);
  const fullPath = path.resolve(installedRoot, relativeFile);
  if (!isPathInsideRoot(installedRoot, fullPath)) {
    throw new Error(
      `configured skill file path escapes package root (${entrypointField}): ${relativeFile}`
    );
  }
  if (!(await pathExists(fullPath))) {
    throw new Error(`missing configured skill file: ${relativeFile}`);
  }
  return fullPath;
}

async function canOverwriteGeneratedTarget(targetDir) {
  const markerPath = path.join(targetDir, 'references', 'book-to-skill.json');
  if (!(await pathExists(markerPath))) {
    return false;
  }

  try {
    const marker = JSON.parse(await fs.readFile(markerPath, 'utf8'));
    return marker.generated_by === 'book-to-skill';
  } catch {
    return false;
  }
}

async function prepareTargetDir(targetDir) {
  if (!(await pathExists(targetDir))) {
    return;
  }

  const overwriteAllowed = await canOverwriteGeneratedTarget(targetDir);
  if (!overwriteAllowed) {
    throw new Error(
      `Refusing to overwrite existing Codex skill directory not generated by book-to-skill: ${targetDir}`
    );
  }

  await fs.rm(targetDir, { recursive: true, force: true });
}

async function generateCodexSkillFromInstalledSkill(installedSkill, options = {}) {
  const manifest = await readInstalledManifest(installedSkill);
  const safeSkillId = validateManifestId(manifest.id);
  const codexSkillsRoot = path.resolve(
    expandHome(options.codexSkillsRoot || '~/.codex/skills')
  );
  const codexSkillPath = path.resolve(codexSkillsRoot, safeSkillId);
  if (!isPathInsideRoot(codexSkillsRoot, codexSkillPath)) {
    throw new Error(`target path escapes codex root: ${safeSkillId}`);
  }
  const referencesDir = path.join(codexSkillPath, 'references');
  const systemPromptFile = manifest.entrypoints.system_prompt_file;
  const inputsSchemaFile = manifest.entrypoints.inputs_schema_file;
  const sourceSystemPath = await ensureConfiguredFile(
    installedSkill.installedPath,
    systemPromptFile,
    'entrypoints.system_prompt_file'
  );
  const sourceInputsSchemaPath = await ensureConfiguredFile(
    installedSkill.installedPath,
    inputsSchemaFile,
    'entrypoints.inputs_schema_file'
  );

  await prepareTargetDir(codexSkillPath);
  await fs.mkdir(referencesDir, { recursive: true });

  await fs.writeFile(path.join(codexSkillPath, 'SKILL.md'), renderSkillMarkdown(manifest), 'utf8');
  await fs.copyFile(sourceSystemPath, path.join(referencesDir, 'system.md'));
  await fs.copyFile(sourceInputsSchemaPath, path.join(referencesDir, 'inputs.schema.json'));
  await fs.writeFile(
    path.join(referencesDir, 'book-to-skill.json'),
    `${JSON.stringify(
      {
        generated_by: 'book-to-skill',
        generated_at: new Date().toISOString(),
        source_skill: {
          id: manifest.id,
          name: manifest.name,
          version: manifest.version,
          installed_path: installedSkill.installedPath,
        },
      },
      null,
      2
    )}\n`,
    'utf8'
  );

  return {
    codexSkillsRoot,
    codexSkillPath,
  };
}

const syncInstalledSkillToCodex = generateCodexSkillFromInstalledSkill;

module.exports = {
  syncInstalledSkillToCodex,
  generateCodexSkillFromInstalledSkill,
};
