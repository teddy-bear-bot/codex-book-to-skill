test('rejects manifest id that escapes codex skills root', async () => {
  const { installedSkill } = await installExampleSkill();
  const manifestPath = path.join(installedSkill.installedPath, 'skill.json');
  const manifest = JSON.parse(await fs.readFile(manifestPath, 'utf8'));
  manifest.id = '../escape-target';
  await fs.writeFile(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`, 'utf8');
  const codexSkillsRoot = await fs.mkdtemp(path.join(os.tmpdir(), 'bts-codex-root-'));

  await assert.rejects(
    () => generateCodexSkillFromInstalledSkill(installedSkill, { codexSkillsRoot }),
    /invalid manifest id|escapes codex root/i
  );
});

